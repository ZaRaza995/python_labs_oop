from model import Patient

def main():
    print(f"Добро пожаловать в {Patient.hospital_name}\n" + "="*30)

    # Создание объектов
    p1 = Patient("Иван", 25, 500)
    p2 = Patient("Иван", 25, 500)
    p3 = Patient("Мария", 30, 50)

    # Демонстрация __str__
    print(f"Создан пациент 1: {p1}")
    print(f"Создан пациент 3: {p3}")

    # Сравнение объектов (__eq__)
    print(f"\nСравнение p1 и p2 (ожидаем True): {p1 == p2}")
    print(f"Сравнение p1 и p3 (ожидаем False): {p1 == p3}")

    # Демонстрация валидации
    print("\nПопытка создать пациента с некорректным возрастом:")
    try:
        invalid_p = Patient("Ошибка", -5)
    except ValueError as e:
        print(f"Поймали ошибку: {e}")

    # Изменение состояний (бизнес-логика)
    print("\nСценарий лечения:")
    p3.get_sick()  # Мария заболела
    p3.treat()     # Пытаемся лечить, но баланс всего 50 (нужно 100)
    
    print("\nПополняем баланс через создание нового объекта или логику (в данном примере просто смотрим на p1):")
    p1.get_sick()
    p1.treat()     # У Ивана 500

    # Работа @property setter
    print(f"\nТекущий возраст {p1.name}: {p1.age}")
    p1.age = 26
    print(f"Новый возраст {p1.name}: {p1.age}")

if __name__ == "__main__":
    main()
